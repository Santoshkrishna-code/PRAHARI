package status_test

import (
	"testing"

	"prahari/services/analytics/internal/domain/status"
)

func TestValidTransitions(t *testing.T) {
	tests := []struct {
		from status.Code
		to   status.Code
	}{
		{status.CodeDraft, status.CodeGenerated},
		{status.CodeDraft, status.CodeFailed},
		{status.CodeGenerated, status.CodeDelivered},
		{status.CodeGenerated, status.CodeFailed},
		{status.CodeFailed, status.CodeDraft},
	}

	for _, tt := range tests {
		t.Run(string(tt.from)+"->"+string(tt.to), func(t *testing.T) {
			if err := status.ValidateTransition(tt.from, tt.to); err != nil {
				t.Errorf("expected transition %s -> %s to be valid, got error: %v", tt.from, tt.to, err)
			}
		})
	}
}

func TestInvalidTransitions(t *testing.T) {
	tests := []struct {
		from status.Code
		to   status.Code
	}{
		{status.CodeDraft, status.CodeDelivered},
		{status.CodeDelivered, status.CodeDraft},
		{status.CodeFailed, status.CodeDelivered},
	}

	for _, tt := range tests {
		t.Run(string(tt.from)+"->"+string(tt.to), func(t *testing.T) {
			if err := status.ValidateTransition(tt.from, tt.to); err == nil {
				t.Errorf("expected transition %s -> %s to be invalid, but got nil", tt.from, tt.to)
			}
		})
	}
}
